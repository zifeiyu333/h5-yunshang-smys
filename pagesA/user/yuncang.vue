				<view class="i-top b-b">
					<text class="time">{{item.crtTime | dateFormat}}</text>
					<text class="status" v-if="item.stock === 0">已售完</text>
					<text class="status" v-if="item.stock > 0">库存数：{{item.stock}}</text>
				</view>
				<view class="goods-box-single" >
					<image class="goods-img" :src="item.primaryPicUrl + '?x-oss-process=image/resize,m_fill,w_100,h_100'" mode="aspectFill"></image>
