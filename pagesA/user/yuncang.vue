<script>
	import uniLoadMore from '@/components/uni-load-more/uni-load-more.vue';
	import empty from "@/components/empty";
	export default {

		components: {
			uniLoadMore,
			empty
		},
		data() {
			return {
				submiting: false,
				list: [],
				loadingType: 'more',
				page: 1
			};
		},
		onLoad() {
			this.loadData()
		},
		//下拉刷新
		onPullDownRefresh() {
			this.loadData('refresh');
		},
		//加载更多
		onReachBottom() {
			this.loadData()
		},
		methods: {
			async loadData(type = 'add', loading) {
				//没有更多直接返回
				if (type === 'add') {
					if (this.loadingType === 'nomore') {
						return;
					}
					this.loadingType = 'loading';
				} else {
					this.loadingType = 'more'
				}
				if (type === 'refresh') {
					this.page = 1
				}
				this.$api.request('mall/api/myGoodsAgent/queryGoodsAgent', {
					page: this.page,
					limit: 10
				}).then(res => {
					let tempList = res.page.records
					if (type === 'refresh') {
						this.list = [];
					}
					this.list = this.list.concat(tempList);
					this.page = res.page.page + 1
					this.loadingType = res.page.totalPages > res.page.page ? 'more' : 'nomore';
					if (type === 'refresh') {
						if (loading == 1) {
							uni.hideLoading()
						} else {
							uni.stopPullDownRefresh()
						}
					}
				}).catch(res => {
					this.$api.msg(res.msg)
				})
			}
		}
	}
</script>

