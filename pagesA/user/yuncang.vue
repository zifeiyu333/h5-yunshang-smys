				</view>
				<view class="price-box">
					可回报金额
					<text class="price">{{item.responsePrice}}</text>
				</view>
			</navigator>
			<!-- <view class="action-box b-t" v-if="item.stock > 0">
				<button :disabled="submiting" class="action-btn" @click="cancelOrder(item)">回购</button>
